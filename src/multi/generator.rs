#[derive(Debug)]
pub(crate) struct Generator<const NDIM: usize>([f64; NDIM]);

impl<const NDIM: usize> Generator<NDIM> {
    pub(crate) const fn new(input: [f64; NDIM]) -> Self {
        Self(input)
    }

    pub(crate) const fn generator(&self) -> &[f64] {
        &self.0
    }
}

impl<const NDIM: usize> IntoIterator for &Generator<NDIM> {
    type Item = [f64; NDIM];
    type IntoIter = Permutations<NDIM>;

    fn into_iter(self) -> Self::IntoIter {
        Permutations::new(&self.0)
    }
}

pub(crate) struct Permutations<const NDIM: usize> {
    initial: bool,
    current: [f64; NDIM],
    ndim: usize,
    j_exchange: usize,
    i_exchange: usize,
}

impl<const NDIM: usize> Permutations<NDIM> {
    pub(crate) const fn new(first: &[f64; NDIM]) -> Self {
        Self {
            initial: true,
            current: *first,
            ndim: NDIM,
            j_exchange: 0,
            i_exchange: 0,
        }
    }
}

impl<const NDIM: usize> Iterator for Permutations<NDIM> {
    type Item = [f64; NDIM];

    fn next(&mut self) -> Option<Self::Item> {
        if self.initial {
            self.initial = false;
            return Some(self.current);
        };

        for i in 0..self.ndim {
            self.current[i] = -self.current[i];
            if self.current[i] < 0.0 {
                return Some(self.current);
            }
        }

        for i in 1..self.ndim {
            if self.current[i - 1] > self.current[i] {
                let value_i = self.current[i];
                self.i_exchange = i - 1;
                for j in 1..=(i / 2) {
                    let value_j = self.current[j - 1];
                    self.current.swap(j - 1, i - j);
                    if value_j <= value_i {
                        self.i_exchange -= 1;
                    };
                    if self.current[j - 1] > value_i {
                        self.j_exchange = j - 1;
                    };
                }
                if self.current[self.i_exchange] <= value_i {
                    self.i_exchange = self.j_exchange;
                };
                self.current[i] = self.current[self.i_exchange];
                self.current[self.i_exchange] = value_i;
                return Some(self.current);
            }
        }

        None
    }
}

